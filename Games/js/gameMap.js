// Função para contar pontos no mapa
function countDots() {
  let count = 0;
  for (let y = 0; y < MAP_HEIGHT; y++) {
    for (let x = 0; x < MAP_WIDTH; x++) {
      if (gameMap[y][x] === MAP_CODES.DOT || gameMap[y][x] === MAP_CODES.POWER_PELLET) {
        count++;
      }
    }
  }
  return count;
}

// Função para encontrar a posição inicial do PacMan
function findPacmanStartPosition() {
  for (let y = 0; y < MAP_HEIGHT; y++) {
    for (let x = 0; x < MAP_WIDTH; x++) {
      if (gameMap[y][x] === MAP_CODES.PACMAN_START) {
        return { x, y };
      }
    }
  }
  return { x: 14, y: 23 }; // Posição padrão se não encontrar
}

// Função para encontrar posições da casa dos fantasmas
function findGhostHousePositions() {
  const positions = [];
  for (let y = 0; y < MAP_HEIGHT; y++) {
    for (let x = 0; x < MAP_WIDTH; x++) {
      if (gameMap[y][x] === MAP_CODES.GHOST_HOUSE) {
        positions.push({ x, y });
      }
    }
  }
  return positions;
}

// Função para desenhar o mapa
function drawMap(ctx) {
  for (let y = 0; y < MAP_HEIGHT; y++) {
    for (let x = 0; x < MAP_WIDTH; x++) {
      const tileType = gameMap[y][x];
      const screenX = x * TILE_SIZE;
      const screenY = y * TILE_SIZE;

      switch (tileType) {
        case MAP_CODES.WALL:
          ctx.fillStyle = WALL_COLOR;
          ctx.fillRect(screenX, screenY, TILE_SIZE, TILE_SIZE);
          break;
        case MAP_CODES.DOT:
          ctx.fillStyle = DOT_COLOR;
          ctx.beginPath();
          ctx.arc(screenX + TILE_SIZE / 2, screenY + TILE_SIZE / 2, 2, 0, Math.PI * 2);
          ctx.fill();
          break;
        case MAP_CODES.POWER_PELLET:
          ctx.fillStyle = POWER_PELLET_COLOR;
          ctx.beginPath();
          ctx.arc(screenX + TILE_SIZE / 2, screenY + TILE_SIZE / 2, 6, 0, Math.PI * 2);
          ctx.fill();
          break;
      }
    }
  }
}

// Função para verificar se uma posição está dentro do mapa
function isPositionInBounds(x, y) {
  return x >= 0 && x < MAP_WIDTH && y >= 0 && y < MAP_HEIGHT;
}

// Função para verificar se uma posição é válida para movimento (não é parede)
function isValidMove(x, y) {
  return isPositionInBounds(x, y) && gameMap[y][x] !== MAP_CODES.WALL;
}

// Função para coletar um ponto ou power pellet
function collectDot(x, y) {
  const tileType = gameMap[y][x];
  let points = 0;

  if (tileType === MAP_CODES.DOT) {
    points = DOT_POINTS;
    gameMap[y][x] = MAP_CODES.EMPTY;
  } else if (tileType === MAP_CODES.POWER_PELLET) {
    points = POWER_PELLET_POINTS;
    gameMap[y][x] = MAP_CODES.EMPTY;
  }

  return { points, isPowerPellet: tileType === MAP_CODES.POWER_PELLET };
}
